package com.visog.jobportal.req.transaction;

public class LanguageKnownReq {
	
	private String USER;
	
	private String LANGUAGE;
	
	private String read;
	
	private String write;
	
	private String speak;
	
	private String proficencylevel;

	public String getUSER() {
		return USER;
	}

	public void setUSER(String uSER) {
		USER = uSER;
	}

	public String getLANGUAGE() {
		return LANGUAGE;
	}

	public void setLANGUAGE(String lANGUAGE) {
		LANGUAGE = lANGUAGE;
	}

	public String getRead() {
		return read;
	}

	public void setRead(String read) {
		this.read = read;
	}

	public String getWrite() {
		return write;
	}

	public void setWrite(String write) {
		this.write = write;
	}

	public String getSpeak() {
		return speak;
	}

	public void setSpeak(String speak) {
		this.speak = speak;
	}

	public String getProficencylevel() {
		return proficencylevel;
	}

	public void setProficencylevel(String proficencylevel) {
		this.proficencylevel = proficencylevel;
	}
	
	
	

}
