package com.visog.jobportal.res.transaction;

import java.util.Date;

public class EmployerRes {

	private String id;

	private String user;
	
	private String industry;
	
	private String employerType;

	private String premiumEmployer;
	
	private String companyName;
	
	private Date registeredDate;
	
	private String registrationNumber;
	
	private String cin;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getIndustry() {
		return industry;
	}

	public void setIndustry(String industry) {
		this.industry = industry;
	}

	public String getEmployerType() {
		return employerType;
	}

	public void setEmployerType(String employerType) {
		this.employerType = employerType;
	}

	public String getPremiumEmployer() {
		return premiumEmployer;
	}

	public void setPremiumEmployer(String premiumEmployer) {
		this.premiumEmployer = premiumEmployer;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public Date getRegisteredDate() {
		return registeredDate;
	}

	public void setRegisteredDate(Date registeredDate) {
		this.registeredDate = registeredDate;
	}

	public String getRegistrationNumber() {
		return registrationNumber;
	}

	public void setRegistrationNumber(String registrationNumber) {
		this.registrationNumber = registrationNumber;
	}

	public String getCin() {
		return cin;
	}

	public void setCin(String cin) {
		this.cin = cin;
	}

	
}
