package com.visog.jobportal.res.transaction;

public class EmployerJobseekerRes {

	private String id;
	
	private String employer;
	
	private String jobSeeker;
	
	private String status;
	
	private String actionDone;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getEmployer() {
		return employer;
	}

	public void setEmployer(String employer) {
		this.employer = employer;
	}

	

	public String getJobSeeker() {
		return jobSeeker;
	}

	public void setJobSeeker(String jobSeeker) {
		this.jobSeeker = jobSeeker;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getActionDone() {
		return actionDone;
	}

	public void setActionDone(String actionDone) {
		this.actionDone = actionDone;
	}

	
	
	
}
